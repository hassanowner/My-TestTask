/*
========================================================
Logical Operators
========================================================
Covered Topics:
- ! Not
- && And
- || Or
========================================================
*/

// ======================================================
// ! Not
// - Reverses boolean value
// ======================================================

console.log(true);
/*
Result:
true
*/

console.log(!true);
/*
Result:
false
*/


// ======================================================
// NOT with Comparison
// ======================================================

console.log(!(20 == "20"));
/*
Result:
false
*/


// ======================================================
// && And
// - All conditions must be true
// ======================================================

console.log(20 == "20" && 20 > 10 && 20 > 30);
/*
Result:
false
*/


// ======================================================
// || Or
// - At least one condition must be true
// ======================================================

console.log(20 == "20" || 20 > 50 || 20 > 30);
/*
Result:
true
*/
