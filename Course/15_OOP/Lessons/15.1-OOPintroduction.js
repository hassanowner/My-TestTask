/*
• What Is OOP?
  - OOP stands for Object-Oriented Programming.
  - It is a programming paradigm (a style or way of writing code).

• Core Idea of OOP:
  - OOP is based on the concept of objects.
  - Objects represent real-life things inside the program (Object Simulation).

• Real-Life Analogy:
  - Example: Car
    • Properties: color, price
    • Methods: walk(), stop()
  - In programming, we design software the same way.

• Object in Programming:
  - An object is a package that contains:
    • Properties (data / characteristics)
    • Methods (functions / actions)

• Methods:
  - Functions that define what the object can do.
  - Example: walk(), stop(), start()

• Properties:
  - Variables that describe the object.
  - Example: color, price, speed

• Is OOP New?
  - No, OOP is not new.
  - The first OOP language was Simula in the 1960s.

• Programming Languages and OOP:
  - Some languages fully support OOP.
  - Some partially support OOP.
  - Some languages do not support OOP.

• Why We Use OOP?
  - To build large and complex software in an organized way.
  - To group related data and behavior together.

• Encapsulation:
  - Hiding certain parts of code inside objects.
  - Prevents messing with important logic.
  - Makes code safer and cleaner.

• Inheritance:
  - Allows creating reusable objects.
  - Objects can inherit properties and methods from other objects.
  - Reduces code repetition.

• Benefits of OOP:
  - Organized and structured code.
  - Reusable components.
  - Easier maintenance and scalability.
  - Clear and readable architecture.
*/
