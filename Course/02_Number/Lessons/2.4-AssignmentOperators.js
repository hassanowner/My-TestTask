// ======================================================
// Assignment Operators
// - Used to update variable values
// ======================================================

let a = 10;

// a = a + 20
a = a + 20;
console.log(a);

/*
Result:
30
*/


// a = a + 70
a = a + 70;
console.log(a);

/*
Result:
100
*/


// a += 100  →  a = a + 100
a += 100;
console.log(a);

/*
Result:
200
*/


// a -= 50  →  a = a - 50
a -= 50;
console.log(a);

/*
Result:
150
*/


// a /= 50  →  a = a / 50
a /= 50;
console.log(a);

/*
Result:
3
*/
