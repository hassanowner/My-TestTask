// ======================================================
// Math.round()
// - Rounds number to nearest integer
// ======================================================

console.log(Math.round(24.4));
/*
Result:
24
*/

console.log(Math.round(24.5));
/*
Result:
25
*/


// ======================================================
// Math.ceil()
// - Rounds number up
// ======================================================

console.log(Math.ceil(24.1));
/*
Result:
25
*/


// ======================================================
// Math.floor()
// - Rounds number down
// ======================================================

console.log(Math.floor(24.9));
/*
Result:
24
*/


// ======================================================
// Math.min()
// - Returns smallest number
// ======================================================

console.log(Math.min(50, -10, 200, 0, 75));
/*
Result:
-10
*/


// ======================================================
// Math.max()
// - Returns largest number
// ======================================================

console.log(Math.max(50, -10, 200, 0, 75));
/*
Result:
200
*/


// ======================================================
// Math.pow()
// - Power calculation
// ======================================================

console.log(Math.pow(3, 3));
/*
Result:
27
*/


// ======================================================
// Math.random()
// - Returns random number between 0 and 1
// ======================================================

console.log(Math.random());
/*
Result:
(Random number between 0 and 1)
*/


// ======================================================
// Math.trunc() [ES6]
// - Removes decimal part
// ======================================================

console.log(Math.trunc(24.8));
/*
Result:
24
*/
