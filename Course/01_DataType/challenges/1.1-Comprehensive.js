// ======================================================
// CHALLENGE 1
// Declare variables with different data types
// ======================================================

let name = "Osama";
let age = 20;
let active = true;

console.log(name);
/*
Result:
Osama
*/

console.log(age);
/*
Result:
20
*/

console.log(active);
/*
Result:
true
*/


// ======================================================
// CHALLENGE 2
// Check the data type of each variable
// ======================================================

console.log(typeof name);
/*
Result:
"string"
*/

console.log(typeof age);
/*
Result:
"number"
*/

console.log(typeof active);
/*
Result:
"boolean"
*/


// ======================================================
// CHALLENGE 3
// String concatenation
// ======================================================

console.log("My name is " + name);
/*
Result:
My name is Osama
*/

console.log("I am " + age + " years old");
/*
Result:
I am 20 years old
*/


// ======================================================
// CHALLENGE 4
// Template literals
// ======================================================

console.log(`My name is ${name}`);
/*
Result:
My name is Osama
*/

console.log(`I am ${age} years old`);
/*
Result:
I am 20 years old
*/


// ======================================================
// CHALLENGE 5
// Use variables in calculations
// ======================================================

console.log(age + 5);
/*
Result:
25
*/

console.log(age - 2);
/*
Result:
18
*/


// ======================================================
// CHALLENGE 6
// Boolean value usage
// ======================================================

console.log(active);
/*
Result:
true
*/

console.log(!active);
/*
Result:
false
*/
